package fr.iutvalence.info.dut.m2107;

public class Attack {
	
	
	private int scope;
	
	private int damages;

	
	public Attack(int scope, int damages) {
		super();
		this.scope = scope;
		this.damages = damages;
	}


	public int getScope() {
		return scope;
	}


	public int getDamages() {
		return damages;
	}
	
	
	
	
	
	
	

}
